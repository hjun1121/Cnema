package com.cnema.theater;

public class SeatDTO {
	private int seat_num;
	private int screen_num;
	private String x_num;
	private int y_num;
	
	public int getSeat_num() {
		return seat_num;
	}
	public void setSeat_num(int seat_num) {
		this.seat_num = seat_num;
	}
	public int getScreen_num() {
		return screen_num;
	}
	public void setScreen_num(int screen_num) {
		this.screen_num = screen_num;
	}
	public String getX_num() {
		return x_num;
	}
	public void setX_num(String x_num) {
		this.x_num = x_num;
	}
	public int getY_num() {
		return y_num;
	}
	public void setY_num(int y_num) {
		this.y_num = y_num;
	}
	
	
}
